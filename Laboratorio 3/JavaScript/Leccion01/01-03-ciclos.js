//while (mientras)
let contador = 0;
while(contador < 3){
    console.log(contador)
    contador++;
}
console.log("Fin del ciclo while");

//do while 
let conteo = 0;
do{
    console.log(conteo)
    conteo++;       //++ Operador Unario, de post incremento
}while(conteo < 3);
console.log("Fin del ciclo do while");